#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char *c;

	c = dest;
	while (n > 0 && *src != '\0')
	{
		*c++ = *src++;
		--n;
	}
	while (n > 0)
	{
		*c++ = '\0';
		--n;
	}
	return (dest);
}
