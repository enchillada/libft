#include "../inc/libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;
	char	c;

	s = s1;
	while (*s1 != '\0')
		s1++;
	while ((n-- > 0) && ((c = *s2++) != '\0'))
		*s1++ = c;
	*s1++ = '\0';
	return (s);
}
